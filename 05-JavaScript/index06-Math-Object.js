// Math works with Number type and not BigInt
{
    console.log(Math.E);
    console.log(Math.LOG10E);
    console.log(Math.LN2);
    console.log(Math.PI);
    console.log(Math.SQRT2);
    console.log(Math.abs(-2));
    console.log(Math.ceil(2.4));
    console.log(Math.floor(2.4));
    console.log(Math.round(2.6));
    console.log(Math.max(4, 2));
    console.log(Math.max(4, 2, 10, 100));
    console.log(Math.min(4, 2));
    console.log(Math.min(4, 5, 10, 100));
    console.log(Math.pow(2, 3));
    console.log(Math.exp(2));
    console.log(Math.log10(100));
    console.log(Math.log2(16));
    console.log(Math.sqrt(10));
    console.log(Math.sin(90));
    console.log(Math.random());
    console.log(Math.imul(5, 7));
}
